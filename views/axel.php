                        <h2> Axel's Bio</h2>
                      <h5>
                           Introducing Axel Hernandez, a second-generation general contractor with a lifelong passion for transforming spaces into works of art. Raised in the world of construction, Axel honed his skills alongside his father, a seasoned general contractor, and quickly developed a deep understanding of the trade.<br>

Upon reaching the age to obtain his contractor's license, Axel wasted no time in officially joining the ranks of licensed contractors. His extensive expertise spans a diverse range of projects, from intricate bathroom remodels to comprehensive roofing, flooring, drywall, painting, trim work, cabinet installations, landscaping, and the nuanced demands of apartment maintenance
<br>
Axel's journey with SleekSpace began when he was commissioned to remodel a basement into a stylish Airbnb by none other than Evett, the dynamic CEO, and her wife Theresa. Impressed by his craftsmanship, attention to detail, and commitment to excellence, they recognized Axel as a valuable asset to the SleekSpace team.
<br>
Residing with his family in Federal Way, WA, Axel brings a unique perspective to every project he undertakes, infusing a personal touch that transforms spaces into functional and aesthetically pleasing environments. His enthusiasm for his work is not only evident in the finished product but is also contagious to those around him.
<br>
Now, as SleekSpace's Lead General Contractor, Axel is set to channel his passion and expertise into elevating the quality and design of every project the company takes on. With Axel at the helm, SleekSpace is poised to deliver unparalleled craftsmanship and innovation in the world of construction and remodeling.

