                        <h2>Founder's Vision</h2>
                        
                      <h5>SleekSpace was born out of Evett's own
                        journey. She saw the need for a
                        company that could handle every aspect
                        of Airbnb property management and
                        couldn't find one that met her
                        standards. <br>So, she decided to build it
                        herself. Inspired by her experience,
                        Evett Van Beek has made it her mission
                        to help others achieve their goals in the
                        world of short-term rentals.<br><br>
                        <b>Career Journey</b><br> Evett Van Beek, the CEO of SleekSpace, boasts a multifaceted career that began in culinary arts at Olive Garden. Her outstanding performance led her to Olive Garden's Culinary Institute in Tuscany. Transitioning to Human Resources post-MBA, Evett held pivotal roles, including as a Field People Officer for McDonald's Corporation and National Vice Chair of their Pride Employee Diversity Network.<br><br>

<b>Recognition and Accolades</b><br> Recognized by Yahoo Finance as one of 2021's Top LGBTQ Executives, Evett's dedication to diversity and strategic leadership has left a lasting impact. Her career also includes roles at Lowe's Home Improvement's ATGStores.com and as the Head of Global People Operations for a Fintech startup.<br><br>

<b>Personal Life</b><br>Born in Gooding, Idaho, Evett moved to the Northwest at 18. Beyond her professional achievements, she's an avid traveler and foodie. Evett is married to Theresa Van Beek, and they have two daughters, Remi (1) and Peyton (4).<br><br>

<b>Conclusion</b><br> Evett Van Beek's journey from culinary arts to CEO exemplifies adaptability and commitment to inclusive leadership. As the driving force behind SleekSpace, she continues to inspire with her transformative approach to business and dedication to fostering diverse and thriving workplace cultures.</h5>
