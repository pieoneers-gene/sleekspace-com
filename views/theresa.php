                        <h2> Theresa's Bio</h2>
                      <h5>
                           Theresa Van Beek is a seasoned professional with over two decades of experience in the customer service industry. As the wife of Evett Van Beek, the CEO of SleekSpace, Theresa assumes the role of Head of Customer Experience for the company. Her journey in customer service began with catering to affluent clientele, where she seamlessly transitioned from being a nanny to a personal assistant and house manager.<br>

Driven by her love for dogs, Theresa leveraged her extensive experience to establish Ultimutt, a successful dog boarding business. Over the years, she cared for not only children but also the cherished family pets. As the offspring grew and the need for a nanny diminished, Theresa gracefully shifted her focus to the family dogs, ultimately turning her passion into a thriving entrepreneurial venture.
<br>
In addition to her achievements in the pet care industry, Theresa possesses a profound interest in the construction field. Collaborating with her wife Evett, she has actively participated in the design and construction of three distinct homes. Theresa's keen eye for detail and commitment to quality have significantly contributed to the success of these projects.<br>

Theresa's presence at SleekSpace serves as a cornerstone of stability and customer-centricity. Known for her polite and calm demeanor, she brings a unique balance to the team, ensuring everyone remains focused on the customer's needs. Theresa's wealth of experience, coupled with her unwavering commitment to excellence, makes her an invaluable asset to SleekSpace and a driving force behind the company's dedication to customer satisfaction.

